<script>
import axios from "axios";
export default {
  data: function () {
    return {
      newSessionParams: {},
      errors: [],
    };
  },
  methods: {
    submit: function () {
      axios
        .post("/sessions", this.newSessionParams)
        .then((response) => {
          axios.defaults.headers.common["Authorization"] = "Bearer " + response.data.jwt;
          localStorage.setItem("jwt", response.data.jwt);
          this.$router.push("/home");
        })
        .catch((error) => {
          console.log(error.response);
          this.errors = ["Invalid email or password."];
          this.email = "";
          this.password = "";
        });
    },
  },
};
</script>

<template>
  <div class="login">
    <form v-on:submit.prevent="submit()">
      <h1>Login</h1>
      <ul>
        <li v-for="error in errors" v-bind:key="error">{{ error }}</li>
      </ul>
      <div>
        <label>Email:</label>
        <input type="text" v-model="newSessionParams.email" placeholder="Enter Email" name="email" required />
      </div>
      <div>
        <label>Password:</label>
        <input type="password" v-model="newSessionParams.password" placeholder="Enter Password" name="psw" required />
      </div>
      <div>
        <label>Password Confirmation:</label>
        <input
          type="password"
          v-model="newSessionParams.password_confirmation"
          placeholder="Password Confirmation"
          name="psw-repeat"
          required
        />
      </div>
      <button type="submit" value="submit" class="submit">Submit</button>
    </form>
  </div>
</template>

